// Arithmetic Operators (+, -, *, /, %, ++, --)

let x = 10;
let y = 22;

console.log(x + y);

console.log(y - x);

console.log(x * y);

console.log(y / x);

console.log(y % x); // Remainder

x++;    // x = x + 1
console.log(x); 

console.log(y++);
console.log(y);

console.log(++x)
console.log(++y)

// Assignment Operators (=, +=, -=, *=, /=, %=)

let salary = 100;

salary += 1000; // salary = salary + 1000
console.log(salary);

salary -= 1000; // salary = salary - 1000
console.log(salary);


